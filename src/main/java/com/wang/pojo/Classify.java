package com.wang.pojo;

public class Classify {
    private Integer id;

    private Integer issueinfoId;

    private Integer significance;

    private Integer quality;

    private Integer component;

    private Integer consequence;

    private Integer code;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getIssueinfoId() {
        return issueinfoId;
    }

    public void setIssueinfoId(Integer issueinfoId) {
        this.issueinfoId = issueinfoId;
    }

    public Integer getSignificance() {
        return significance;
    }

    public void setSignificance(Integer significance) {
        this.significance = significance;
    }

    public Integer getQuality() {
        return quality;
    }

    public void setQuality(Integer quality) {
        this.quality = quality;
    }

    public Integer getComponent() {
        return component;
    }

    public void setComponent(Integer component) {
        this.component = component;
    }

    public Integer getConsequence() {
        return consequence;
    }

    public void setConsequence(Integer consequence) {
        this.consequence = consequence;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }
}